                            <h2 class="heading">Обучение дайвингу</h2>
                        </div>
                        <p>Весь курс делится на две части: закрытую и открытую воду. Под термином «закрытая вода» подразумеваются занятия в бассейне. В начальном курсе обучения их пять. Во время этих учебных погружений. Вы под руководством инструктора доводите до автоматизма выполнение определенных упражнений, которые позволяют без причинения вреда собственному здоровью выходить из неприятных ситуаций, которые могут возникнуть в процессе погружения с аквалангом. Если обучающемуся недостаточно пяти стандартных занятий, мы будем заниматься ещё, пока не достигнем нужного результата.</p>

                        <p>Вторая часть курса называется «открытая вода» и состоит из четырех погружений в реальных условиях, в течении которых вы демонстрируете инструктору выполнение упражнений из курса «закрытой воды», но уже находясь на реальной глубине и в воде, которая может довольно ощутимо «резать» глаза. У очень большого количества учеников возникает вопрос: зачем нам открытая вода, ели мы все навыки уже освоили в бассейне? Поверьте – это просто необходимо. Как показывает практика, у значительного числа бассейновых отличников, возникают реальные проблемы при первых морских погружениях. Сказываются такие стрессовые факторы как течения, соленая вода, наличие реальной глубины, отсутствие кафельных бортиков бассейна. И гораздо комфортнее Вы будите чувствовать себя, проходя через это испытание под руководством профессионала.</p>

                        <p>И вот только после того, как Вы закончите сдачу «открытой воды», сможете получить сертификат международного образца, который дает Вам право совершать погружения с аквалангом в любой точке нашего шарика.</p>

                        <p>А теперь о длительности обучения. Как уже упоминалось выше, начальный курс состоит из пяти занятий, включающих в себя теорию и практическую часть. То есть на него нужно будет потратить пять вечеров. Одно занятие длится около трех часов.</p>

                        <p>Можно, конечно, проводить в день и по два таких занятия, но тогда исчезнет чувство комфорта, зато придет чувство глубокой усталости.</p>

                        <p>Вообщем, надо изыскать пять свободных вечеров. Причем совсем не обязательно, чтобы они были подряд. Мы всегда готовы составить такой график, что обучение дайвингу в Москве будет комфортным для Вас.</p>

                        <p>На «открытую воду» уйдет два дня, то есть по два зачётных погружения в день. Итого имеем семь дней. Вот здесь и стоит подумать над тем: стоит ли тратить весь свой отпуск на море на прохождение курсов или пройти курсы обучения дайвингу в Москве, а на море сдать только «открытую воду»</p>
